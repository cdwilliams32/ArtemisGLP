        then
            poseidon3:orderAttack(EDP1)
            poseidon2:orderAttack(EDP1)
            poseidon1:orderAttack(EDP1)
        end
        if EDP2:isValid() and not EDP1:isValid()
        then
            poseidon3:orderAttack(EDP2)
            poseidon2:orderAttack(EDP2)
            poseidon1:orderAttack(EDP2)
        end
